#!/bin/bash
# 시스템 상태 점검 스크립트

echo "=========================================="
echo "시스템 점검 보고서"
echo "점검 시각: $(date '+%Y-%m-%d %H:%M:%S')"
echo "=========================================="
echo ""

# 1. CPU 사용률
echo "[CPU 정보]"
top -bn1 | grep "Cpu(s)" | awk '{print "CPU 사용률: " $2}'
echo ""

# 2. 메모리 사용률
echo "[메모리 정보]"
free -h | grep Mem | awk '{print "전체: " $2 " / 사용중: " $3 " / 사용률: " int($3/$2*100) "%"}'
echo ""

# 3. 디스크 사용률
echo "[디스크 정보]"
df -h / | tail -1 | awk '{print "루트 파티션: " $5 " 사용중 (" $3 "/" $2 ")"}'
echo ""

# 4. 실행 중인 프로세스 수
echo "[프로세스 정보]"
echo "실행 중인 프로세스: $(ps aux | wc -l)개"
echo ""

echo "=========================================="
echo "점검 완료"
echo "=========================================="
